package net.tecgurus.spring.mvc.beans;

/**
 * @author Allan Flores Rojas
 *
 */
public class Cliente {
	
	private String nombre;
	private String apePat;
	private String apeMat;
	private String telCte;
	private String dirCte;
	private Integer idEdoCte;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the apePat
	 */
	public String getApePat() {
		return apePat;
	}
	/**
	 * @param apePat the apePat to set
	 */
	public void setApePat(String apePat) {
		this.apePat = apePat;
	}
	/**
	 * @return the apeMat
	 */
	public String getApeMat() {
		return apeMat;
	}
	/**
	 * @param apeMat the apeMat to set
	 */
	public void setApeMat(String apeMat) {
		this.apeMat = apeMat;
	}
	/**
	 * @return the telCte
	 */
	public String getTelCte() {
		return telCte;
	}
	/**
	 * @param telCte the telCte to set
	 */
	public void setTelCte(String telCte) {
		this.telCte = telCte;
	}
	/**
	 * @return the dirCte
	 */
	public String getDirCte() {
		return dirCte;
	}
	/**
	 * @param dirCte the dirCte to set
	 */
	public void setDirCte(String dirCte) {
		this.dirCte = dirCte;
	}
	/**
	 * @return the idEdoCte
	 */
	public Integer getIdEdoCte() {
		return idEdoCte;
	}
	/**
	 * @param idEdoCte the idEdoCte to set
	 */
	public void setIdEdoCte(Integer idEdoCte) {
		this.idEdoCte = idEdoCte;
	}
	
	

}
